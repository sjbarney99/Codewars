// Messi is a soccer player with goals in three leagues:

// LaLiga
// Copa del Rey
// Champions.
// Complete the goals function to return his total goals (the sum) for all three leagues.

// Note: the parameter for this function will always be a valid number.

const goals = (laLigaGoals, copaDelReyGoals, championsLeagueGoals) => {
    let total = laLigaGoals + copaDelReyGoals + championsLeagueGoals
    return total
}